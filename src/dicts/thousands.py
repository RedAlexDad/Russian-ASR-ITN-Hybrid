"""
Модуль словарей: тысячи, миллионы, миллиарды — умножители.

Зачем это нужно:
  В русском языке тысячи/миллионы работают не как сложение, а как умножение.
  "пять тысяч" = 5 * 1000 = 5000, а не 5 + 1000.
  "двести пятьдесят тысяч" = (200 + 50) * 1000 = 250000.

  Поэтому эти слова помечены как is_mult=True (умножители).
  Парсер видит umножитель и умножает накопленное значение, а не складывает.

Формат: слово -> (значение, mag)
  mag: 4=тысячи, 5=миллионы, 6=миллиарды
"""

# Тысячи — самый частый умножитель в данных (цены, бюджеты)
# ASR-ошибки: "тыщ", "тыщи" — частые искажения "тысяч"/"тысячи"
THOUSANDS = {
    "тысяча": (1000, 4),
    "тысячи": (1000, 4),
    "тысяч": (1000, 4),
    "тысячу": (1000, 4),
    "тысяче": (1000, 4),
    "тысячей": (1000, 4),
    "тыщ": (1000, 4),
    "тыщи": (1000, 4),
}

# Миллионы — встречаются реже, но в бюджетах крупных компаний
MILLIONS = {
    "миллион": (1000000, 5),
    "миллиона": (1000000, 5),
    "миллионов": (1000000, 5),
    "миллионом": (1000000, 5),
}

# Миллиарды — редко, но для полноты
BILLIONS = {
    "миллиард": (1000000000, 6),
    "миллиарда": (1000000000, 6),
    "миллиардов": (1000000000, 6),
}

# Множество слов-умножителей для быстрой проверки is_mult
# Используется в lexicon.lookup_word() чтобы вернуть флаг is_mult=True
MULTIPLIERS = set()
for d in (THOUSANDS, MILLIONS, BILLIONS):
    MULTIPLIERS.update(d.keys())
